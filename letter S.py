import turtle


def main():
    my_turtle = turtle.Turtle()
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(50)
    my_turtle.right(90)
    my_turtle.forward(50)
    my_turtle.right(90)
    my_turtle.forward(50)


if __name__ == "__main__":
    main()
